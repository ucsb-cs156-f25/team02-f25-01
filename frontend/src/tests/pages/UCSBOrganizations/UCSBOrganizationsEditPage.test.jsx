import { fireEvent, render, waitFor, screen } from "@testing-library/react";
import { QueryClient, QueryClientProvider } from "@tanstack/react-query";
import { MemoryRouter } from "react-router";
import UCSBOrganizationsEditPage from "main/pages/UCSBOrganizations/UCSBOrganizationsEditPage";

import { apiCurrentUserFixtures } from "fixtures/currentUserFixtures";
import { systemInfoFixtures } from "fixtures/systemInfoFixtures";
import axios from "axios";
import AxiosMockAdapter from "axios-mock-adapter";
import mockConsole from "tests/testutils/mockConsole";

const mockToast = vi.fn();
vi.mock("react-toastify", async (importOriginal) => {
  const originalModule = await importOriginal();
  return {
    ...originalModule,
    toast: vi.fn((x) => mockToast(x)),
  };
});

const mockNavigate = vi.fn();
vi.mock("react-router", async (importOriginal) => {
  const originalModule = await importOriginal();
  return {
    ...originalModule,
    useParams: vi.fn(() => ({
      id: "17",
    })),
    Navigate: vi.fn((x) => {
      mockNavigate(x);
      return null;
    }),
  };
});

let axiosMock;
describe("UCSBOrganizationsEditPage tests", () => {
  describe("when the backend doesn't return data", () => {
    beforeEach(() => {
      axiosMock = new AxiosMockAdapter(axios);
      axiosMock.reset();
      axiosMock.resetHistory();
      axiosMock
        .onGet("/api/currentUser")
        .reply(200, apiCurrentUserFixtures.userOnly);
      axiosMock
        .onGet("/api/systemInfo")
        .reply(200, systemInfoFixtures.showingNeither);
      axiosMock.onGet("/api/ucsborganizations", { params: { orgCode: "17" } }).timeout();
    });

    afterEach(() => {
      mockToast.mockClear();
      mockNavigate.mockClear();
      axiosMock.restore();
      axiosMock.resetHistory();
    });

    const queryClient = new QueryClient();
    test("renders header but table is not present", async () => {
      const restoreConsole = mockConsole();

      render(
        <QueryClientProvider client={queryClient}>
          <MemoryRouter>
            <UCSBOrganizationsEditPage />
          </MemoryRouter>
        </QueryClientProvider>,
      );
      await screen.findByText("Edit UCSB Organization");
      expect(screen.queryByTestId("UCSBOrganization-orgTranslationShort")).not.toBeInTheDocument();
      restoreConsole();
    });
  });

  describe("tests where backend is working normally", () => {
    beforeEach(() => {
      axiosMock = new AxiosMockAdapter(axios);
      axiosMock.reset();
      axiosMock.resetHistory();
      axiosMock
        .onGet("/api/currentUser")
        .reply(200, apiCurrentUserFixtures.userOnly);
      axiosMock
        .onGet("/api/systemInfo")
        .reply(200, systemInfoFixtures.showingNeither);
      axiosMock.onGet("/api/ucsborganizations", { params: { orgCode: "17" } }).reply(200, {
        orgCode: "17",
        orgTranslationShort: "Freebirds",
        orgTranslation: "Burritos",
        inactive: false,
      });
      axiosMock.onPut("/api/ucsborganizations").reply(200, {
        orgCode: "17",
        orgTranslationShort: "Freebirds World Burrito",
        orgTranslation: "Really big Burritos",
        inactive: false,
      });
    });

    afterEach(() => {
      mockToast.mockClear();
      mockNavigate.mockClear();
      axiosMock.restore();
      axiosMock.resetHistory();
    });

    const queryClient = new QueryClient();

    test("Is populated with the data provided", async () => {
      render(
        <QueryClientProvider client={queryClient}>
          <MemoryRouter>
            <UCSBOrganizationsEditPage />
          </MemoryRouter>
        </QueryClientProvider>,
      );

      await screen.findByTestId("UCSBOrganizationsForm-orgCode");

      const orgCodeField = screen.getByTestId("UCSBOrganizationsForm-orgCode");
      const orgTranslationShortField = screen.getByTestId("UCSBOrganizationsForm-orgTranslationShort");
      const orgTranslationField = screen.getByTestId("UCSBOrganizationsForm-orgTranslation");
      const inactiveField = screen.getByTestId("UCSBOrganizationsForm-inactive");
      const submitButton = screen.getByTestId("UCSBOrganizationsForm-submit");

      expect(orgCodeField).toBeInTheDocument();
      expect(orgCodeField).toHaveValue("17");
      expect(orgTranslationShortField).toBeInTheDocument();
      expect(orgTranslationShortField).toHaveValue("Freebirds");
      expect(orgTranslationField).toBeInTheDocument();
      expect(orgTranslationField).toHaveValue("Burritos");
      expect(inactiveField).toBeInTheDocument();
      expect(inactiveField).toHaveValue("false");
      expect(submitButton).toHaveTextContent("Update");

      fireEvent.change(orgTranslationShortField, {
        target: { value: "Freebirds World Burrito" },
      });
      fireEvent.change(orgTranslationField, {
        target: { value: "Totally Giant Burritos" },
      });
      fireEvent.change(inactiveField, { target: { value: "true" } });
      fireEvent.click(submitButton);

      await waitFor(() => expect(mockToast).toBeCalled());
      expect(mockToast).toBeCalledWith(
        "UCSB Organization Updated - orgCode: 17 orgTranslationShort: Freebirds World Burrito orgTranslation: Really big Burritos inactive: false",
      );

      expect(mockNavigate).toBeCalledWith({ to: "/ucsborganizations" });

      expect(axiosMock.history.put.length).toBe(1); // times called
      expect(axiosMock.history.put[0].params).toEqual({ orgCode: "17" });
      expect(axiosMock.history.put[0].data).toBe(
        JSON.stringify({
          orgCode: "17",
          orgTranslationShort: "Freebirds World Burrito",
          orgTranslation: "Totally Giant Burritos",
          inactive: "true",
        }),
      ); // posted object
    });

    test("Changes when you click Update", async () => {
      render(
        <QueryClientProvider client={queryClient}>
          <MemoryRouter>
            <UCSBOrganizationsEditPage />
          </MemoryRouter>
        </QueryClientProvider>,
      );

      await screen.findByTestId("UCSBOrganizationsForm-orgCode");

      const orgCodeField = screen.getByTestId("UCSBOrganizationsForm-orgCode");
      const orgTranslationShortField = screen.getByTestId("UCSBOrganizationsForm-orgTranslationShort");
      const orgTranslationField = screen.getByTestId("UCSBOrganizationsForm-orgTranslation");
      const inactiveField = screen.getByTestId("UCSBOrganizationsForm-inactive");
      const submitButton = screen.getByTestId("UCSBOrganizationsForm-submit");

      expect(orgCodeField).toHaveValue("17");
      expect(orgTranslationShortField).toHaveValue("Freebirds");
      expect(orgTranslationField).toHaveValue("Burritos");
      expect(inactiveField).toHaveValue("false");
      expect(submitButton).toBeInTheDocument();

      fireEvent.change(orgTranslationShortField, {
        target: { value: "Freebirds World Burrito" },
      });
      fireEvent.change(orgTranslationField, { target: { value: "Big Burritos" } });
      fireEvent.change(inactiveField, { target: { value: "true" } });

      fireEvent.click(submitButton);

      await waitFor(() => expect(mockToast).toBeCalled());
      expect(mockToast).toBeCalledWith(
        "UCSB Organization Updated - orgCode: 17 orgTranslationShort: Freebirds World Burrito orgTranslation: Really big Burritos inactive: false",
      );
      expect(mockNavigate).toBeCalledWith({ to: "/ucsborganizations" });
    });
  });
});
